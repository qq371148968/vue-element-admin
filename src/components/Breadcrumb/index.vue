<template>
  <el-breadcrumb class="app-breadcrumb" separator="/">

    <!-- vue 中的过渡动画 transition-group enter和leave的样式在文件src/styles/transition.scss 中定义 -->
    <transition-group name="breadcrumb">

      <!-- 初始化或者路由切换时， 调用$route.matched初始化levelList；输出一个数组，包含当前路由的所有嵌套路径片段的路由记录 -->
      <el-breadcrumb-item v-for="(item,index) in levelList" :key="item.path">
        <span v-if="item.redirect==='noRedirect'||index==levelList.length-1" class="no-redirect">{{ item.meta.title }}</span>
        <a v-else @click.prevent="handleLink(item)">{{ item.meta.title }}</a>
      </el-breadcrumb-item>
    </transition-group>
  </el-breadcrumb>
</template>

<script>
import pathToRegexp from 'path-to-regexp'

export default {
  data() {
    return {
      levelList: null
    }
  },
  watch: {
    $route(route) {
      // if you go to the redirect page, do not update the breadcrumbs
      if (route.path.startsWith('/redirect/')) {
        return
      }
      this.getBreadcrumb()
    }
  },
  created() {
    this.getBreadcrumb()
  },
  methods: {
    getBreadcrumb() {
      // only show routes with meta.title
      let matched = this.$route.matched.filter(item => item.meta && item.meta.title)
      const first = matched[0]

      // 默认会把Dashboard放面包屑的第一个位置
      if (!this.isDashboard(first)) {
        matched = [{ path: '/dashboard', meta: { title: 'Dashboard' }}].concat(matched)
      }

      this.levelList = matched.filter(item => item.meta && item.meta.title && item.meta.breadcrumb !== false)
    },
    isDashboard(route) {
      const name = route && route.name
      if (!name) {
        return false
      }
      return name.trim().toLocaleLowerCase() === 'Dashboard'.toLocaleLowerCase()
    },
    pathCompile(path) {
      // To solve this problem https://github.com/PanJiaChen/vue-element-admin/issues/561
      const { params } = this.$route
      var toPath = pathToRegexp.compile(path)
      return toPath(params)
      /**
       * 知识点：compile作用：快速填充 url 字符串的参数值。
       * var pathToRegexp = require('path-to-regexp')
       * var url = '/user/:id/:name'
       * var data = {id: 10001, name: 'bob'}
       * console.log(pathToRegexp.compile(url)(data))
       * 打印结果：
       * /user/10001/bob
       */
    },
    // 面包屑的点击跳转事件
    handleLink(item) {
      const { redirect, path } = item
      if (redirect) {
        this.$router.push(redirect)
        return
      }
      this.$router.push(this.pathCompile(path))
    }
  }
}
</script>

